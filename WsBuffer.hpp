#pragma once

#include <QImage>

//*****************************************************************************
//*****************************************************************************
/**
 * @brief The WsBuffer class
 */
//*****************************************************************************
class WsBuffer : public QImage
{
public:


   //*****************************************************************************
   //*****************************************************************************
   /**
    * @brief WsBuffer constructor
    * @param w
    * @param h
    * @param fmt
    */
   //*****************************************************************************
   WsBuffer( quint16 w, quint16 h, QImage::Format fmt );

   //*****************************************************************************
   //*****************************************************************************
   /**
    * @brief ~WsBuffer destructor
    */
   //*****************************************************************************
   ~WsBuffer();

   //*****************************************************************************
   //*****************************************************************************
   /**
    * @brief dataSize - size of the output buffer in words
    * @return
    */
   //*****************************************************************************
   quint32 dataSize() { return outBufSize_; }

   //*****************************************************************************
   //*****************************************************************************
   /**
    * @brief outBuf - pointer to internal output buffer
    * @return
    */
   //*****************************************************************************
   quint16* outBuf() { return outBuffer_; }

protected:

   // output buffer size
   quint32 outBufSize_;

   // output buffer
   quint16* outBuffer_;

   // original image size
   quint16 orgWidth_;
   quint16 orgHeight_;

   // original image format
   QImage::Format orgFormat_;
};

